let programmingLanguages = [
  "C#",
  "Python",
  "PHP",
  "Javascript",
  "Kotlin",
  "Java",
  "Ruby",
  "Go",
  "Rust",
  "Cobol",
  "Bash",
  "C++"
];
let lengthOfList = programmingLanguages.length
console.log(lengthOfList)
// first Element
console.log(programmingLanguages[0])
// last Element
console.log(programmingLanguages[lengthOfList-1])
// push fügt am Ende der Liste etwas hinzu
programmingLanguages.push("C")
console.log(programmingLanguages)
// pop entfernt das letzte Element in der Liste
programmingLanguages.pop()
console.log(programmingLanguages)
// unshift fügt am Anfang der Liste etwas hinzu
programmingLanguages.unshift("Powershell")
console.log(programmingLanguages)
// shift entfernt am Anfang der Liste das Element
programmingLanguages.shift()
console.log(programmingLanguages)
